  getAvailableTools(): readonly string[] {
    return [
      // ── Primary pipeline (Distill → Read → Write) ──
      'scrape_and_index_profile',
      'read_distilled_section',
      'dispatch_extraction',
      'write_core_identity',
      'write_combine_metrics',
      'write_rankings',
      'write_season_stats',
      'write_recruiting_activity',
      'write_calendar_events',
      'write_athlete_videos',
      'write_timeline_post',
      'search_nxt1_platform',
      'query_nxt1_platform_data',
      'list_nxt1_data_views',
      'query_nxt1_data',

      // ── Verification & enrichment ──
      'search_web',

      // ── Raw fallback scraping ──
      'scrape_webpage',
      'map_website',

      // ── Social media scraping (Apify-hosted actors) ──
      'scrape_twitter',

      // ── General Apify MCP tools (dynamic actor discovery & execution) ──
      'search_apify_actors',
      'get_apify_actor_details',
      'call_apify_actor',
      'get_apify_actor_output',

      // ── Live browsing (auth-protected pages) ──
      'open_live_view',
      'navigate_live_view',
      'interact_with_live_view',
      'read_live_view',
      'close_live_view',

      // ── Communication ──
      'ask_user',
      'scan_timeline_posts',
    ];
  }

  getModelRouting(): ModelRoutingConfig {
    // Uses the "data_heavy" tier — Qwen 3.6 Plus (1M context) handles
    // massive season stats, game logs, and bulk scraping with large contexts.
    // No modelOverride needed: the tier system resolves to Qwen directly
    // with Haiku/GPT-4o-mini as fallbacks.
    return MODEL_ROUTING_DEFAULTS['data_heavy'];
  }
