/**
 * Build a storage path from a template.
 *
 * @param template - Path template from STORAGE_PATHS
 * @param params - Parameters to substitute
 * @returns Resolved storage path
 *
 * @example
 * buildPath(STORAGE_PATHS.PROFILE_PHOTO, { userId: 'abc123' })
 * // Returns: 'users/abc123/profile'
 */
export function buildPath(template: string, params: Record<string, string>): string {
  return Object.entries(params).reduce(
    (path, [key, value]) => path.replace(`{${key}}`, value),
    template
  );
}

/**
 * Get thumbnail path for an original image.
 * Extension places thumbnails in SAME directory with size suffix.
 *
 * @param originalPath - Path to original image
 * @param size - Thumbnail size key
 * @param format - Image format
 * @returns Path to thumbnail
 *
 * @example
 * getThumbnailPath('users/123/profile/avatar_1234567890.jpg', 'SMALL', 'WEBP')
 * // Returns: 'users/123/profile/avatar_1234567890_200x200.webp'
 */
export function getThumbnailPath(
  originalPath: string,
  size: keyof typeof THUMBNAIL_SIZES,
  format: keyof typeof IMAGE_FORMATS = 'WEBP'
): string {
  const dir = originalPath.substring(0, originalPath.lastIndexOf('/'));
  const fileName = originalPath.substring(originalPath.lastIndexOf('/') + 1);
  const baseName = fileName.substring(0, fileName.lastIndexOf('.'));

  const sizeConfig = THUMBNAIL_SIZES[size];
  const ext = IMAGE_FORMATS[format];

  // Extension places thumbnails in same directory (no subdirectory)
  // Format: {baseName}_{width}x{height}.{format}
  return `${dir}/${baseName}_${sizeConfig.suffix}.${ext}`;
}

/**
 * Get all thumbnail paths for an original image.
 * Extension generates 3 formats × 3 sizes = 9 thumbnails total.
 *
 * @param originalPath - Path to original image
 * @returns Object with paths for all sizes/formats
 */
export function getAllThumbnailPaths(originalPath: string): {
  small: { jpg: string; webp: string; png: string };
  medium: { jpg: string; webp: string; png: string };
  large: { jpg: string; webp: string; png: string };
} {
  return {
    small: {
      jpg: getThumbnailPath(originalPath, 'SMALL', 'JPEG'),
      webp: getThumbnailPath(originalPath, 'SMALL', 'WEBP'),
      png: getThumbnailPath(originalPath, 'SMALL', 'PNG'),
    },
    medium: {
      jpg: getThumbnailPath(originalPath, 'MEDIUM', 'JPEG'),
      webp: getThumbnailPath(originalPath, 'MEDIUM', 'WEBP'),
      png: getThumbnailPath(originalPath, 'MEDIUM', 'PNG'),
    },
    large: {
      jpg: getThumbnailPath(originalPath, 'LARGE', 'JPEG'),
      webp: getThumbnailPath(originalPath, 'LARGE', 'WEBP'),
      png: getThumbnailPath(originalPath, 'LARGE', 'PNG'),
    },
  };
}

/**
 * Check if a path is monitored by the resize extension.
 *
 * @param path - Storage path to check
 * @returns True if extension will process uploads to this path
 */
export function isExtensionMonitoredPath(path: string): boolean {
  // Extension is configured to monitor: /users/*/profile
  return /^users\/[^/]+\/profile/.test(path);
}

/**
 * Validate image file for upload.
 *
 * @param mimeType - File MIME type
 * @param size - File size in bytes
 * @param category - Upload category
 * @returns Validation result
 */
export function validateImageUpload(
  mimeType: string,
  size: number,
  category: 'PROFILE_PHOTO' | 'COVER_PHOTO' | 'TEAM_LOGO'
): { valid: boolean; error?: string } {
  if (!ALLOWED_IMAGE_TYPES.includes(mimeType as (typeof ALLOWED_IMAGE_TYPES)[number])) {
    return {
      valid: false,
      error: `Invalid file type. Allowed: ${ALLOWED_IMAGE_TYPES.join(', ')}`,
    };
  }

  const maxSize = MAX_FILE_SIZES[category];
  if (size > maxSize) {
    const maxMB = maxSize / (1024 * 1024);
    return {
      valid: false,
      error: `File too large. Maximum: ${maxMB}MB`,
    };
  }

  return { valid: true };
}
