/**
 * Supported image formats for thumbnails.
 * Extension generates ALL formats for each size.
 * Order matches extension config: jpeg, webp, png
 */
export const IMAGE_FORMATS = {
  /** JPEG - universal compatibility, good compression */
  JPEG: 'jpg',
  /** WebP - best compression, modern browsers */
  WEBP: 'webp',
  /** PNG - lossless, supports transparency */
  PNG: 'png',
} as const;

