  /** Small thumbnail - avatars, list items */
  SMALL: { width: 200, height: 200, suffix: '200x200' },
  /** Medium thumbnail - profile cards, preview */
  MEDIUM: { width: 400, height: 400, suffix: '400x400' },
  /** Large thumbnail - detailed view, high-res preview */
  LARGE: { width: 800, height: 800, suffix: '800x800' },
