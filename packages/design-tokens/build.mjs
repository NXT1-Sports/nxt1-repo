/**
 * Sanitize a key for SCSS variable names
 * SCSS variables can't start with numbers or contain dots
 */
function toScssVarName(key) {
  return key
    .replace(/\./g, '_') // Replace dots with underscores
    .replace(/^(\d)/, '_$1'); // Prefix leading numbers with underscore
}

function generateScssTokens(primitiveTokens, semanticTokens, allTokens) {
  const lines = [
    '// ============================================',
    '// NXT1 Design Tokens - SCSS Variables',
    '// @generated by build.mjs - DO NOT EDIT MANUALLY',
    '// @source tokens.json/',
    '// ============================================',
    '',
    '// Usage:',
    '//   @use "@nxt1/design-tokens/dist/scss/tokens" as tokens;',
    '//   color: tokens.$nxt1-color-primary;',
    '//   background: var(--nxt1-color-bg-primary);',
    '',
  ];

  // SCSS variables from primitives
  const primitiveFlat = flattenTokens(primitiveTokens);

  lines.push('// PRIMITIVE TOKENS');
  for (const [key, token] of Object.entries(primitiveFlat)) {
    const resolved = resolveReference(token.value, allTokens);
    const scssKey = toScssVarName(key);
    lines.push(`$nxt1-${scssKey}: ${resolved};`);
  }

  lines.push('');
  lines.push('// SEMANTIC TOKENS (CSS variable references for runtime theming)');

  // Generate SCSS vars that reference CSS custom properties
  const darkTokens = semanticTokens.theme?.dark;
  if (darkTokens) {
    const darkFlat = flattenTokens(darkTokens);
    for (const [key] of Object.entries(darkFlat)) {
      let cssKey = sanitizeCssKey(
        key.replace(/^color-background-/, 'color-bg-').replace(/^color-feedback-/, 'color-')
      );
      const scssKey = toScssVarName(cssKey);
      lines.push(`$nxt1-${scssKey}: var(--nxt1-${cssKey});`);
    }
  }

  lines.push('');
  lines.push('// TOKEN MAPS (for programmatic access)');
  lines.push('$nxt1-colors: (');

  // Color map
  const colorTokens = flattenTokens(primitiveTokens.color || {});
  for (const [key, token] of Object.entries(colorTokens)) {
    const resolved = resolveReference(token.value, allTokens);
    lines.push(`  "${key}": ${resolved},`);
  }
  lines.push(');');

  lines.push('');
  lines.push('$nxt1-spacing: (');
  const spacingTokens = flattenTokens(primitiveTokens.spacing || {});
  for (const [key, token] of Object.entries(spacingTokens)) {
    lines.push(`  "${key}": ${token.value},`);
  }
  lines.push(');');

  return lines.join('\n');
}

