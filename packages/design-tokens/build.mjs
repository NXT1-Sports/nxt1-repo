// JSON GENERATION (Resolved)
// ============================================

function generateResolvedJson(primitiveTokens, semanticTokens, componentTokens, allTokens) {
  const resolved = {
    $schema: 'https://design-tokens.github.io/community-group/format/',
    $description: 'NXT1 Design Tokens - Resolved values (generated)',
    primitive: {},
    semantic: {},
    component: {},
  };

  // Resolve primitives
  const primitiveFlat = flattenTokens(primitiveTokens);
  for (const [key, token] of Object.entries(primitiveFlat)) {
    resolved.primitive[key] = {
      value: resolveReference(token.value, allTokens),
      type: token.type,
    };
  }

  // Resolve semantic (dark theme)
  if (semanticTokens.theme?.dark) {
    const darkFlat = flattenTokens(semanticTokens.theme.dark);
    resolved.semantic.dark = {};
    for (const [key, token] of Object.entries(darkFlat)) {
      resolved.semantic.dark[key] = {
        value: resolveReference(token.value, allTokens),
        type: token.type,
      };
    }
  }

  // Resolve semantic (light theme)
  if (semanticTokens.theme?.light) {
    const lightFlat = flattenTokens(semanticTokens.theme.light);
    resolved.semantic.light = {};
    for (const [key, token] of Object.entries(lightFlat)) {
      resolved.semantic.light[key] = {
        value: resolveReference(token.value, allTokens),
        type: token.type,
      };
    }
  }

  // Resolve components
  if (componentTokens.component) {
    const componentFlat = flattenTokens(componentTokens);
    for (const [key, token] of Object.entries(componentFlat)) {
      resolved.component[key] = {
        value: resolveReference(token.value, allTokens),
        type: token.type,
      };
    }
  }

  return JSON.stringify(resolved, null, 2);
}

