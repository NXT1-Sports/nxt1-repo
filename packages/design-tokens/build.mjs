/**
 * Sanitize a token key for CSS custom property names
 * CSS vars shouldn't have dots (causes parsing issues in some tools)
 * @param {string} key - The token key (e.g., "spacing-0.5")
 * @returns {string} - Sanitized key (e.g., "spacing-0_5")
 */
function sanitizeCssKey(key) {
  return key.replace(/\./g, '_');
}

function toCssVarName(path) {
  return `--nxt1-${path.join('-').replace(/\./g, '_')}`;
}

function flattenTokens(obj, path = [], result = {}) {
  for (const [key, value] of Object.entries(obj)) {
    if (key.startsWith('$')) continue; // Skip metadata

    const newPath = [...path, key];

    if (value && typeof value === 'object' && value.$value !== undefined) {
      // This is a token
      result[newPath.join('-')] = {
        value: value.$value,
        type: value.$type,
        description: value.$description,
        path: newPath,
      };
    } else if (value && typeof value === 'object') {
      // Nested group
      flattenTokens(value, newPath, result);
    }
  }

  return result;
}

function generateCssTokens(primitiveTokens, allTokens) {
  const flattened = flattenTokens(primitiveTokens);
  const lines = [
    '/**',
    ' * @fileoverview NXT1 Design Tokens - CSS Custom Properties',
    ' * @generated by build.mjs - DO NOT EDIT MANUALLY',
    ' * @source tokens.json/',
    ' */',
    '',
    ':root {',
  ];

  for (const [key, token] of Object.entries(flattened)) {
    const cssKey = sanitizeCssKey(key);
    const cssVar = `--nxt1-${cssKey}`;
    const resolved = resolveReference(token.value, allTokens);

    if (token.description) {
      lines.push(`  /* ${token.description} */`);
    }
    lines.push(`  ${cssVar}: ${resolved};`);
  }

  lines.push('}');
  return lines.join('\n');
}

function generateThemeCss(themeTokens, themeName, allTokens) {
  const flattened = flattenTokens(themeTokens);
  const selector =
    themeName === 'dark'
      ? ':root,\n[data-theme="dark"],\n.dark-theme'
      : '[data-theme="light"],\n.light-theme';

  const colorScheme = themeName === 'dark' ? 'dark' : 'light';

  const lines = [
    '/**',
    ` * @fileoverview NXT1 ${themeName.charAt(0).toUpperCase() + themeName.slice(1)} Theme`,
    ' * @generated by build.mjs - DO NOT EDIT MANUALLY',
    ' */',
    '',
    `${selector} {`,
    `  color-scheme: ${colorScheme};`,
    '',
  ];

  for (const [key, token] of Object.entries(flattened)) {
    // Convert semantic paths like "background-primary" to "bg-primary"
    let cssKey = sanitizeCssKey(
      key
        .replace(/^color-background-/, 'color-bg-')
        .replace(/^color-text-on/, 'color-text-on-')
        .replace(/^color-feedback-/, 'color-')
    );

    const cssVar = `--nxt1-${cssKey}`;
    const resolved = resolveReference(token.value, allTokens);
    lines.push(`  ${cssVar}: ${resolved};`);
  }

  lines.push('}');
  return lines.join('\n');
}

function generateCombinedCss(primitiveTokens, semanticTokens, allTokens) {
  const lines = [
    '/**',
    ' * @fileoverview NXT1 Design Tokens - Complete CSS',
    ' * @generated by build.mjs - DO NOT EDIT MANUALLY',
    ' * @source tokens.json/',
    ' *',
    ' * This file contains all design tokens as CSS custom properties.',
    ' * Import this single file to get all tokens with dark theme as default.',
    ' *',
    ' * Usage:',
    ' *   @import "@nxt1/design-tokens/dist/css/tokens.css";',
    ' *',
    ' * Theme switching:',
    ' *   <html data-theme="light"> for light theme',
    ' *   <html data-theme="dark"> or default for dark theme',
    ' */',
    '',
    '/* ============================================',
    '   PRIMITIVE TOKENS',
    '   Raw values - colors, spacing, typography',
    '   ============================================ */',
    '',
  ];

  // Primitive tokens
  const primitiveFlat = flattenTokens(primitiveTokens);
  lines.push(':root {');

  // Group by category for readability
  const categories = {};
  for (const [key, token] of Object.entries(primitiveFlat)) {
    const category = key.split('-')[0];
    if (!categories[category]) categories[category] = [];
    categories[category].push({ key, token });
  }

  for (const [category, tokens] of Object.entries(categories)) {
    lines.push(`  /* ${category.toUpperCase()} */`);
    for (const { key, token } of tokens) {
      const cssKey = sanitizeCssKey(key);
      const resolved = resolveReference(token.value, allTokens);
      lines.push(`  --nxt1-${cssKey}: ${resolved};`);
    }
    lines.push('');
  }

  lines.push('}');
  lines.push('');

  // Dark theme (default)
  lines.push('/* ============================================');
  lines.push('   DARK THEME (DEFAULT)');
  lines.push('   ============================================ */');
  lines.push('');

  const darkTokens = semanticTokens.theme?.dark;
  if (darkTokens) {
    lines.push(':root,');
    lines.push('[data-theme="dark"],');
    lines.push('.dark-theme {');
    lines.push('  color-scheme: dark;');
    lines.push('');

    const darkFlat = flattenTokens(darkTokens);
    for (const [key, token] of Object.entries(darkFlat)) {
      let cssKey = sanitizeCssKey(
        key.replace(/^color-background-/, 'color-bg-').replace(/^color-feedback-/, 'color-')
      );
      const resolved = resolveReference(token.value, allTokens);
      lines.push(`  --nxt1-${cssKey}: ${resolved};`);
    }
    lines.push('}');
  }

  lines.push('');

  // Light theme
  lines.push('/* ============================================');
  lines.push('   LIGHT THEME');
  lines.push('   ============================================ */');
  lines.push('');

  const lightTokens = semanticTokens.theme?.light;
  if (lightTokens) {
    lines.push('[data-theme="light"],');
    lines.push('.light-theme {');
    lines.push('  color-scheme: light;');
    lines.push('');

    const lightFlat = flattenTokens(lightTokens);
    for (const [key, token] of Object.entries(lightFlat)) {
      let cssKey = sanitizeCssKey(
        key.replace(/^color-background-/, 'color-bg-').replace(/^color-feedback-/, 'color-')
      );
      const resolved = resolveReference(token.value, allTokens);
      lines.push(`  --nxt1-${cssKey}: ${resolved};`);
    }
    lines.push('}');
  }

  return lines.join('\n');
}

