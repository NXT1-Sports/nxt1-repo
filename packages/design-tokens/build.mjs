function ensureDir(dir) {
  if (!existsSync(dir)) {
    mkdirSync(dir, { recursive: true });
  }
}

function cleanDist() {
  if (existsSync(DIST_DIR)) {
    rmSync(DIST_DIR, { recursive: true });
  }
}

function build() {
  console.log('🎨 NXT1 Design Token Build');
  console.log('==========================\n');

  // Clean previous build
  console.log('🧹 Cleaning dist/...');
  cleanDist();

  // Create directories
  ensureDir(join(DIST_DIR, 'css'));
  ensureDir(join(DIST_DIR, 'css/themes'));
  ensureDir(join(DIST_DIR, 'scss'));
  ensureDir(join(DIST_DIR, 'js'));
  ensureDir(join(DIST_DIR, 'json'));

  // Load tokens
  console.log('📦 Loading tokens from tokens.json/...');
  const { primitiveTokens, semanticTokens, componentTokens } = loadTokens();

  // Merge all tokens for reference resolution
  const allTokens = { ...primitiveTokens, ...semanticTokens, ...componentTokens };

  // Generate CSS
  console.log('🎨 Generating CSS...');
  const combinedCss = generateCombinedCss(primitiveTokens, semanticTokens, allTokens);
  writeFileSync(join(DIST_DIR, 'css/tokens.css'), combinedCss);
  console.log('   ✓ dist/css/tokens.css');

  // Generate individual theme files
  if (semanticTokens.theme?.dark) {
    const darkCss = generateThemeCss(semanticTokens.theme.dark, 'dark', allTokens);
    writeFileSync(join(DIST_DIR, 'css/themes/dark.css'), darkCss);
    console.log('   ✓ dist/css/themes/dark.css');
  }

  if (semanticTokens.theme?.light) {
    const lightCss = generateThemeCss(semanticTokens.theme.light, 'light', allTokens);
    writeFileSync(join(DIST_DIR, 'css/themes/light.css'), lightCss);
    console.log('   ✓ dist/css/themes/light.css');
  }

  // Generate SCSS
  console.log('📝 Generating SCSS...');
  const scssTokens = generateScssTokens(primitiveTokens, semanticTokens, allTokens);
  writeFileSync(join(DIST_DIR, 'scss/_tokens.scss'), scssTokens);
  console.log('   ✓ dist/scss/_tokens.scss');

  // Generate JS
  console.log('⚡ Generating JavaScript...');
  const jsTokens = generateJsTokens(primitiveTokens, semanticTokens, componentTokens, allTokens);
  writeFileSync(join(DIST_DIR, 'js/tokens.mjs'), jsTokens);
  console.log('   ✓ dist/js/tokens.mjs');

  // Generate TypeScript definitions
  const dtsTokens = generateTypeDefinitions(primitiveTokens, componentTokens);
  writeFileSync(join(DIST_DIR, 'js/tokens.d.ts'), dtsTokens);
  console.log('   ✓ dist/js/tokens.d.ts');

  // Generate resolved JSON
  console.log('📋 Generating resolved JSON...');
  const resolvedJson = generateResolvedJson(
    primitiveTokens,
    semanticTokens,
    componentTokens,
    allTokens
  );
  writeFileSync(join(DIST_DIR, 'json/resolved.json'), resolvedJson);
  console.log('   ✓ dist/json/resolved.json');

  // Generate Ionic SCSS files
  console.log('🎨 Generating Ionic SCSS...');
  ensureDir(IONIC_DIR);

  const ionicColors = generateIonicColors(primitiveTokens);
  writeFileSync(join(IONIC_DIR, '_colors.scss'), ionicColors);
  console.log('   ✓ ionic/_colors.scss');

  const ionicTypography = generateIonicTypography(primitiveTokens);
  writeFileSync(join(IONIC_DIR, '_typography.scss'), ionicTypography);
  console.log('   ✓ ionic/_typography.scss');

  console.log('\n✅ Build complete!\n');
  console.log('Generated files:');
  console.log('  CSS:   dist/css/tokens.css (combined)');
  console.log('         dist/css/themes/dark.css');
  console.log('         dist/css/themes/light.css');
  console.log('  SCSS:  dist/scss/_tokens.scss');
  console.log('  Ionic: ionic/_colors.scss');
  console.log('         ionic/_typography.scss');
  console.log('  JS:    dist/js/tokens.mjs');
  console.log('         dist/js/tokens.d.ts');
  console.log('  JSON:  dist/json/resolved.json\n');
}

// Run build
build();
